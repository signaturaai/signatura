export function getCVVersionByApplicationId(applicationId: string): CVVersion | undefined {
  return mockCVVersions.find((cv) => cv.job_application_id === applicationId)
}

/**
 * Helper function to get active applications
 */
export function getActiveApplications(): JobApplication[] {
  return mockJobApplications.filter(
    (app) => !['rejected', 'withdrawn', 'accepted'].includes(app.application_status)
  )
}

/**
 * Helper function to get applications with interviews
 */
export function getApplicationsWithInterviews(): JobApplication[] {
  return mockJobApplications.filter(
    (app) => app.application_status === 'interview_scheduled'
  )
}
